.section .text
.extern interrupt_dispatch

.global gdt_flush
gdt_flush:
    mov 4(%esp), %eax
    lgdt (%eax)
    mov $0x10, %ax
    mov %ax, %ds
    mov %ax, %es
    mov %ax, %fs
    mov %ax, %gs
    mov %ax, %ss
    ljmp $0x08, $1f
1:
    ret

.global idt_load
idt_load:
    mov 4(%esp), %eax
    lidt (%eax)
    ret

.macro ISR_NOERR number
.global isr\number
isr\number:
    push $0
    push $\number
    jmp interrupt_common
.endm

.macro ISR_ERR number
.global isr\number
isr\number:
    push $\number
    jmp interrupt_common
.endm

