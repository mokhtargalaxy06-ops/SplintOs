    pusha
    push %ds
    push %es
    push %fs
    push %gs
    mov $0x10, %ax
    mov %ax, %ds
    mov %ax, %es
    cld
    push %esp
    call interrupt_dispatch
    add $4, %esp
    mov %eax, %esp
    pop %gs
